## Numeros romanos

print('Escribe un numero entre el 1 y el 10 y te mostrare su equivalente en numero romano ')

n1 = int (input('Dame un numero  '))


if n1== 1:
    print(f'{n1} = I')

elif n1==2:
    print(f'{n1} = II')

elif n1==3:
    print(f'{n1} = III')
elif n1==4:
    print(f'{n1} = IV')
elif n1==5:
    print(f'{n1} = V')
elif n1==6:
    print(f'{n1} = VI')
elif n1==7:
    print(f'{n1} = VII')
elif n1==8:
    print(f'{n1} = VIII')
elif n1==9:
    print(f'{n1} = IX')
elif n1==10:
    print(f'{n1} = X')
    
else:
    print('ERROR ')